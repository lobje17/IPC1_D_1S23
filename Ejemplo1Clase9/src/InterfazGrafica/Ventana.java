    private void SeleccionarFichaX(int posInicialX, int posFinalX, int posInicialY, int posFinalY, int veocidad){
        JLabel tmp = X1;
        switch (fichaX) {
            case 1:
                tmp = X1;
                break;
            case 2:
                tmp = X2;
                break;
            case 3:
                tmp = X3;
                break;
            case 4:
                tmp = X4;
                break;
            case 5:
                tmp = X5;
                break;
        }
        mf.moverFiccha(tmp, posInicialX, posFinalX, posInicialY, posFinalY, veocidad);
        fichaX++;
    }
    
    
    private void SeleccionarFichaO(int posInicialX, int posFinalX, int posInicialY, int posFinalY, int veocidad){
        JLabel tmp = O1;
        switch (fichaO) {
            case 1:
                tmp = O1;
                break;
            case 2:
                tmp = O2;
                break;
            case 3:
                tmp = O3;
                break;
            case 4:
                tmp = O4;
                break;
            case 5:
                tmp = O5;
                break;
        }
        mf.moverFiccha(tmp, posInicialX, posFinalX, posInicialY, posFinalY, veocidad);
        fichaO++;
    }
    
    private void siguienteFicha(int posInicialX, int posFinalX, int posInicialY, int posFinalY, int veocidad){
        if(tipoFicha) SeleccionarFichaX(posInicialX, posFinalX, posInicialY, posFinalY, veocidad);
        else SeleccionarFichaO(posInicialX, posFinalX, posInicialY, posFinalY, veocidad);
        this.tipoFicha = !tipoFicha;
    }
