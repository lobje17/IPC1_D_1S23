    private void llenarTabla(){
        this.modelo = new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Nombre");
        modelo.addColumn("Productos");
                
        int cantidad = this.categorias.cantidad();
        Categoria tmp;
        for (int i = 0; i < cantidad; i++) {
            tmp = this.categorias.getCategoria(i);
            //addRowTable(i, codigo, porc);
            addRowTable(i, tmp.getNombre(), tmp.getProductos().Cantidad());
        }
        
        this.tableCategorias.setModel(modelo);
        this.tableCategorias.getColumnModel().getColumn(0).setMaxWidth(15);
        this.tableCategorias.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
    private void addRowTable(int id, String nombre, int cantida){
        modelo.addRow(new Object[]{String.valueOf(id), nombre, String.valueOf(cantida)});
        this.tableCategorias.setModel(modelo);
    }
    
    private void mensaje(String mensaje){
        JOptionPane.showMessageDialog(null, mensaje);
    }

