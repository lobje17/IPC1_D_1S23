    private void registrar()
    {
        boolean result = this.categorias.registroCategoria(nombre);
        
        if(result){
                mensaje("Registrado");     
                addRowTable(categorias.cantidad(), nombre, 0);
                llenarTabla();
        }
        else mensaje("La categoria con nombre "+nombre+" ya existe");
        
        this.txtNombre.setText("");
    }
    
    private boolean camposLlenos()
    {
        String msj = "";
        this.nombre = this.txtNombre.getText();
        
        if(nombre.equals("")) msj += "Codigo obligatorio\n";
        if(!msj.equals("")){
            mensaje(msj);
            return false;
        }
        return true;
    }
